package DataTypes;

public class floats {
    public static void main(String[] args) {
        // Types of floats

        // float
        float floatMin = 1.40129846432481707e-45f;
        float floatMax = 3.40282346638528860e+38f;

        System.out.println("Lo minimo de un float es: " + floatMin);
        System.out.println("Lo maximo de un float es: " + floatMax);

        // double
        double doubleMin = 4.94065645841246544e-324d;
        double doubleMax = 1.79769313486231570e+308d;

        System.out.println("Lo minimo de un double es: " + doubleMin);
        System.out.println("Lo maximo de un double es: " + doubleMax);

    }
}

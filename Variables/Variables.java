package Variables;

public class Variables {
    public static void main(String[] args) {
        // Tipo de dato, nombre de la variable y lo que se le asigna a la variables
        // Integer
        // Inicializar una variable y asignarle un valor
        int speed = 10;
        System.out.println(speed);

        // inicializar una variable sin asignarle un valor
        int salary;
        // Asignarle un valor a la variable
        salary = 1000;
        System.out.println(salary);

        // String
        String name = "Carlos";
        System.out.println(name);
    }
}

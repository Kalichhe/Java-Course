package Operators;

public class AssignmentOperators {
    public static void main(String[] args) {
        // Assignment Operators

        // +=
        int a = 0;
        a += 1;
        System.out.println(a);

        // -=
        int b = 1;
        b -= 1;
        System.out.println(b);

        // *=
        int c = 2;
        c *= 2;
        System.out.println(c);

        // /=
        int d = 4;
        d /= 2;
        System.out.println(d);

        // %=
        int e = 10;
        e %= 2;
        System.out.println(e);

    }
}
